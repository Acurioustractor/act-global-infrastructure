#!/usr/bin/env node
/**
 * For every page in config/notion-database-ids.json that the most recent
 * audit marked ARCHIVED, fetch and display:
 *   - precise last_edited_time
 *   - last_edited_by (the user / integration that flipped archived=true)
 *   - parent type + parent id (where the page lived before trash)
 *   - public URL (best-effort)
 *
 * Goal: tell Ben whether the 17 archived money-stack pages were trashed
 * deliberately (one big cleanup, same user, same minute) or accidentally
 * (different times, different users) so the restore call is informed.
 *
 * READ-ONLY. Does not restore anything. Restore is a separate step (see
 * restore-archived-notion-pages.mjs once we know what to restore).
 *
 * Usage:
 *   node scripts/inspect-archived-notion-pages.mjs            # markdown table
 *   node scripts/inspect-archived-notion-pages.mjs --json     # raw JSON
 */
import { Client } from '@notionhq/client'
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __filename = fileURLToPath(import.meta.url)
const __dirname = dirname(__filename)
await import(join(__dirname, 'lib/load-env.mjs'))

const REPO_ROOT = join(__dirname, '..')
const CFG_PATH = join(REPO_ROOT, 'config/notion-database-ids.json')
const COCKPIT_DIR = join(REPO_ROOT, 'wiki/cockpit')

const argv = process.argv.slice(2)
const JSON_OUT = argv.includes('--json')

// Subset that came back ARCHIVED in the 2026-05-07 audit. Refresh if the
// audit re-runs and the archived set changes.
const ARCHIVED_KEYS = [
  'moneyFramework',
  'pilePage_voice', 'pilePage_flow', 'pilePage_ground', 'pilePage_grants',
  'moneyInAlignment', 'moneyOutAlignment',
  'moneySyncPage', 'weeklyDigest',
  'cashForecast', 'cashScenarios', 'budgetActual', 'kpisPage',
  'cy26StrategyPlan', 'planningRhythm',
  'financeSurfaceDesign', 'dashboardWalkthrough',
]

if (!process.env.NOTION_TOKEN) {
  console.error('[inspect-archived] NOTION_TOKEN env var not set')
  process.exit(1)
}

const notion = new Client({ auth: process.env.NOTION_TOKEN })
const cfg = JSON.parse(readFileSync(CFG_PATH, 'utf-8'))

function titleFromPage(page) {
  const props = page.properties || {}
  for (const k of Object.keys(props)) {
    const p = props[k]
    if (p && p.type === 'title' && Array.isArray(p.title)) {
      return p.title.map((t) => t.plain_text).join('').trim() || '(untitled)'
    }
  }
  return '(untitled)'
}

async function fetchUserName(userId) {
  try {
    const u = await notion.users.retrieve({ user_id: userId })
    return u.name || u.type || userId.slice(0, 8)
  } catch {
    return userId.slice(0, 8) + ' (lookup failed)'
  }
}

async function inspect(key) {
  const id = cfg[key]
  try {
    const page = await notion.pages.retrieve({ page_id: id })
    const editedBy = page.last_edited_by?.id ? await fetchUserName(page.last_edited_by.id) : '(unknown)'
    return {
      key,
      id,
      kind: 'page',
      title: titleFromPage(page),
      archived: page.archived,
      last_edited_at: page.last_edited_time,
      last_edited_by_id: page.last_edited_by?.id,
      last_edited_by_name: editedBy,
      parent_type: page.parent?.type,
      parent_id: page.parent?.page_id || page.parent?.database_id || null,
      url: page.url,
    }
  } catch (e) {
    return { key, id, error: e.message.slice(0, 120) }
  }
}

async function main() {
  console.log(`[inspect-archived] inspecting ${ARCHIVED_KEYS.length} archived pages...`)
  const records = []
  for (let i = 0; i < ARCHIVED_KEYS.length; i++) {
    process.stdout.write(`\r[inspect-archived] ${i + 1}/${ARCHIVED_KEYS.length}: ${ARCHIVED_KEYS[i].padEnd(30)}`)
    records.push(await inspect(ARCHIVED_KEYS[i]))
  }
  process.stdout.write('\n')

  if (JSON_OUT) {
    console.log(JSON.stringify(records, null, 2))
    return
  }

  const TODAY = new Date().toISOString().slice(0, 10)
  const lines = []
  lines.push(`# Archived Notion pages inspection — ${TODAY}`, '')
  lines.push(`> Generated by \`scripts/inspect-archived-notion-pages.mjs\`. Read-only — does not restore anything.`, '')

  // Group by editor + minute bucket so accidental-vs-deliberate clusters surface.
  const buckets = {}
  for (const r of records) {
    if (r.error) continue
    const minute = r.last_edited_at?.slice(0, 16) || '(unknown)'
    const key = `${r.last_edited_by_name} · ${minute}`
    ;(buckets[key] ||= []).push(r)
  }

  lines.push(`## Edit clusters (by user + minute)`, '')
  lines.push(`If multiple pages share a single (user, minute) cluster, they were almost certainly archived in one batch action.`, '')
  for (const [k, rows] of Object.entries(buckets).sort((a, b) => b[1].length - a[1].length)) {
    lines.push(`### ${k} — ${rows.length} page(s)`, '')
    for (const r of rows) lines.push(`- \`${r.key}\` — ${r.title}`)
    lines.push('')
  }

  lines.push(`## Full detail`, '')
  lines.push(`| Key | Title | Archived | Last edited | By | Parent type | URL |`)
  lines.push(`|---|---|:--:|---|---|---|---|`)
  for (const r of records) {
    if (r.error) {
      lines.push(`| \`${r.key}\` | (error) | - | - | - | - | _${r.error}_ |`)
      continue
    }
    const archived = r.archived ? '✓' : '✗'
    const url = r.url ? `[link](${r.url})` : '-'
    lines.push(`| \`${r.key}\` | ${r.title.slice(0, 40)} | ${archived} | ${r.last_edited_at} | ${r.last_edited_by_name} | ${r.parent_type} | ${url} |`)
  }
  lines.push('')

  lines.push(`## Restore plan`, '')
  lines.push(`To restore: \`node scripts/restore-archived-notion-pages.mjs --keys <key1>,<key2>,...\` (script not yet built — confirm restore plan first).`, '')

  const out = lines.join('\n')
  const outPath = join(COCKPIT_DIR, `archived-notion-inspection-${TODAY}.md`)
  writeFileSync(outPath, out)
  console.log(out)
  console.log(`\n[inspect-archived] wrote ${outPath}`)
}

main().catch((err) => {
  console.error('[inspect-archived] fatal:', err.stack || err.message)
  process.exit(1)
})
