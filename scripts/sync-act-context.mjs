function injectIntoClaudeMd(claudeMdPath, block) {
  if (!existsSync(claudeMdPath)) {
    return { status: 'missing', path: claudeMdPath }
  }
  let content = readFileSync(claudeMdPath, 'utf8')
  const beginIdx = content.indexOf(BEGIN)
  const endIdx = content.indexOf(END)

  let newContent
  if (beginIdx !== -1 && endIdx !== -1) {
    // Replace existing block
    const before = content.slice(0, beginIdx).trimEnd()
    const after = content.slice(endIdx + END.length).trimStart()
    newContent = `${before}\n\n${block}\n\n${after}`.trim() + '\n'
  } else if (beginIdx === -1 && endIdx === -1) {
    // Append to end
    newContent = content.trimEnd() + '\n\n---\n\n' + block + '\n'
  } else {
    return { status: 'corrupt', path: claudeMdPath, reason: 'partial markers, manual fix needed' }
  }

  if (newContent === content) {
    return { status: 'unchanged', path: claudeMdPath }
  }

  return { status: 'changed', path: claudeMdPath, oldLen: content.length, newLen: newContent.length, newContent }
}

function main() {
  console.log(`[sync-act-context] mode: ${APPLY ? 'APPLY' : 'DRY-RUN'}`)
  console.log(`[sync-act-context] source: ${SOURCE}`)

  const block = generateContextBlock()
  const targets = ONLY ? [ONLY] : TARGET_REPOS

  console.log(`[sync-act-context] targets: ${targets.length}\n`)

  const results = { changed: 0, unchanged: 0, missing: 0, corrupt: 0 }
  for (const repo of targets) {
    const claudeMd = join(repo, 'CLAUDE.md')
    const result = injectIntoClaudeMd(claudeMd, block)
    results[result.status]++

    const tag = {
      changed: 'CHANGED',
      unchanged: 'UNCHANGED',
      missing: 'MISSING ',
      corrupt: 'CORRUPT ',
    }[result.status]
    console.log(`  [${tag}] ${repo}`)
    if (result.status === 'corrupt') console.log(`             ${result.reason}`)
    if (result.status === 'changed' && APPLY) {
      writeFileSync(claudeMd, result.newContent)
    }
  }

  console.log(`\n[sync-act-context] summary: changed=${results.changed} unchanged=${results.unchanged} missing=${results.missing} corrupt=${results.corrupt}`)
  if (!APPLY && results.changed > 0) {
    console.log(`[sync-act-context] re-run with --apply to write changes`)
  }
}

main()
