#!/usr/bin/env node
/**
 * wiki-save-synthesis.mjs — the compounding loop
 *
 * When the /wiki query operation produces a non-trivial answer with citations,
 * call this script to file the answer as a permanent synthesis article. The next
 * query builds on top of it.
 *
 * This is the heart of the Karpathy second-brain pattern: questions feed back into
 * the wiki, so curiosity literally makes the system smarter.
 *
 * Usage:
 *   node scripts/wiki-save-synthesis.mjs "<question>" "<answer markdown>" [cite1 cite2 ...]
 *
 *   # Or pipe the answer body via stdin:
 *   cat answer.md | node scripts/wiki-save-synthesis.mjs "<question>" --stdin [citations...]
 *
 * Output: writes to wiki/synthesis/<slug>.md, updates wiki/synthesis/index.md,
 * and appends a `synthesis` event to wiki/log.md.
 */
import { readFileSync, writeFileSync, existsSync, readdirSync } from 'fs'
import { join } from 'path'
import { logWikiEvent } from './wiki-log.mjs'

const ROOT = process.cwd()
const SYNTHESIS_DIR = join(ROOT, 'wiki', 'synthesis')
const INDEX_PATH = join(SYNTHESIS_DIR, 'index.md')

function slugify(text) {
  return String(text)
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .slice(0, 80)
}

function uniqueSlug(baseSlug) {
  if (!existsSync(join(SYNTHESIS_DIR, `${baseSlug}.md`))) return baseSlug
  let i = 2
  while (existsSync(join(SYNTHESIS_DIR, `${baseSlug}-${i}.md`))) i++
  return `${baseSlug}-${i}`
}

export function saveSynthesis({ question, answer, citations = [] }) {
  if (!question || !answer) {
    throw new Error('saveSynthesis requires { question, answer }')
  }

  const today = new Date().toISOString().slice(0, 10)
  const baseSlug = slugify(question)
  const slug = uniqueSlug(baseSlug)
  const filePath = join(SYNTHESIS_DIR, `${slug}.md`)

  const citationLines = citations.length > 0
    ? citations.map(c => `- [[${c.replace(/\.md$/, '').split('/').pop()}]]`).join('\n')
    : '_(no citations recorded — re-run /wiki query to add)_'

  const frontmatter = `---
title: "${question.replace(/"/g, '\\"').slice(0, 120)}"
status: Synthesis
cluster: synthesis
created: ${today}
---

`

  const body = `# ${question}

> Synthesised from the wiki on ${today}. This article was generated by a query and saved back into Tractorpedia so the next question can build on top of it.

## Answer

${answer.trim()}

## Citations

${citationLines}

## Backlinks

- [[index|Synthesis Index]]
- [[../index|Tractorpedia Index]]
`

  writeFileSync(filePath, frontmatter + body, 'utf8')

  // Update synthesis/index.md to list the new article
  if (existsSync(INDEX_PATH)) {
    let indexContent = readFileSync(INDEX_PATH, 'utf8')
    const newLine = `- [[${slug}|${question.slice(0, 100)}]] — ${today}`
    if (indexContent.includes('_(This index is auto-maintained.')) {
      indexContent = indexContent.replace(
        /_\(This index is auto-maintained\.[^)]*\)_/,
        `${newLine}\n\n_(This index is auto-maintained. New synthesis articles will appear here as they are created.)_`,
      )
    } else if (indexContent.match(/## Synthesis articles\s*\n/)) {
      indexContent = indexContent.replace(
        /(## Synthesis articles\s*\n)/,
        `$1\n${newLine}\n`,
      )
    } else {
      indexContent += `\n${newLine}\n`
    }
    writeFileSync(INDEX_PATH, indexContent, 'utf8')
  }

  try {
    logWikiEvent(
      'synthesis',
      `"${question.slice(0, 100)}" → ${citations.length} citations`,
      [`wiki/synthesis/${slug}.md`],
    )
  } catch (e) {
    console.error('  ⚠ wiki-log append failed:', e.message)
  }

  return { slug, filePath }
}

// CLI invocation
if (import.meta.url === `file://${process.argv[1]}`) {
  const args = process.argv.slice(2)
  if (args.length < 2) {
    console.error('Usage: node scripts/wiki-save-synthesis.mjs "<question>" "<answer>" [citation1 citation2 ...]')
    console.error('       node scripts/wiki-save-synthesis.mjs "<question>" --stdin [citations...]')
    process.exit(1)
  }
  const question = args[0]
  let answer
  let citations
  if (args[1] === '--stdin') {
    answer = readFileSync(0, 'utf8')
    citations = args.slice(2)
  } else {
    answer = args[1]
    citations = args.slice(2)
  }
  const { slug, filePath } = saveSynthesis({ question, answer, citations })
  console.log(`✓ Synthesis saved: ${slug}`)
  console.log(`  → ${filePath}`)
}
